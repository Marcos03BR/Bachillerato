// Positivo o negativo. Comprobador de positivo o negativo.
   
var num1;

num1 = prompt("Introduzca un número.");

if (num1 > 0){
    
    document.write("El número es positivo.");
    
} else if (num1 == 0){
    
    document.write("El número es 0.");
    
} else if (num1 < 0){
    
    document.write("El número es negativo.");
    
}